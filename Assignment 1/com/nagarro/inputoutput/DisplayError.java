package com.nagarro.inputoutput;

/**
 * This class acts as a view to display any errors made by the user during input
 * 
 * @author rishabgandhar
 *
 */
public class DisplayError {
	/**
	 * This method displays any errors made by the user during input
	 * 
	 * @param exception
	 */
	public static void display(String exception) {
		System.out.println(exception);
	}
}
